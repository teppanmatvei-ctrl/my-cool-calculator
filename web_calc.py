import streamlit as st

# –ó–∞–≥–æ–ª–æ–≤–æ–∫ —Å—Ç—Ä–∞–Ω–∏—Ü—ã
st.title("üßÆ –ú–æ–π –ø–µ—Ä–≤—ã–π –≤–µ–±-–∫–∞–ª—å–∫—É–ª—è—Ç–æ—Ä")

st.write("–í–≤–µ–¥–∏ —á–∏—Å–ª–∞ –∏ –≤—ã–±–µ—Ä–∏ –æ–ø–µ—Ä–∞—Ü–∏—é:")

# –°–æ–∑–¥–∞–µ–º –¥–≤–∞ –ø–æ–ª—è –¥–ª—è –≤–≤–æ–¥–∞ —á–∏—Å–µ–ª
num1 = st.number_input("–ü–µ—Ä–≤–æ–µ —á–∏—Å–ª–æ", value=0.0)
num2 = st.number_input("–í—Ç–æ—Ä–æ–µ —á–∏—Å–ª–æ", value=0.0)

# –°–ø–∏—Å–æ–∫ –æ–ø–µ—Ä–∞—Ü–∏–π –≤ –≤—ã–ø–∞–¥–∞—é—â–µ–º –º–µ–Ω—é
operation = st.selectbox("–ß—Ç–æ —Å–¥–µ–ª–∞—Ç—å?", ["–°–ª–æ–∂–∏—Ç—å", "–í—ã—á–µ—Å—Ç—å", "–£–º–Ω–æ–∂–∏—Ç—å", "–†–∞–∑–¥–µ–ª–∏—Ç—å"])

# –ö–Ω–æ–ø–∫–∞ –¥–ª—è —Ä–∞—Å—á–µ—Ç–∞
if st.button("–ü–æ—Å—á–∏—Ç–∞—Ç—å"):
    if operation == "–°–ª–æ–∂–∏—Ç—å":
        res = num1 + num2
    elif operation == "–í—ã—á–µ—Å—Ç—å":
        res = num1 - num2
    elif operation == "–£–º–Ω–æ–∂–∏—Ç—å":
        res = num1 * num2
    elif operation == "–†–∞–∑–¥–µ–ª–∏—Ç—å":
        res = num1 / num2 if num2 != 0 else "–ù–∞ –Ω–æ–ª—å –¥–µ–ª–∏—Ç—å –Ω–µ–ª—å–∑—è!"
    
    # –í—ã–≤–æ–¥–∏–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç –∫—Ä–∞—Å–∏–≤—ã–º —Ü–≤–µ—Ç–æ–º
    st.success(f"–†–µ–∑—É–ª—å—Ç–∞—Ç: {res}")